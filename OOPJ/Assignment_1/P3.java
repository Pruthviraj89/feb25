public class P3 {

    public static void main(String[] args) {
        
        // Declare a double variable
        double doubleValue = 25.75;
        
        // Convert double to int using typecasting
        int intValue = (int) doubleValue;
        
        // Display both values
        System.out.println("Original Double Value: " + doubleValue);  // Prints the double value
        System.out.println("Converted Int Value: " + intValue);      // Prints the converted int value
    }
}
